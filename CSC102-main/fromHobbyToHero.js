function forLoopFunction()
{
    var i = 1;
    for (i = 1; i < 4; i++)
    {
        alert("For Loop i = " + i);
    }
}
function whileFunction()
{
    var i = 10;
    while (i>0)
    {
        alert("while loop i = " + i);
        i--;
    }
}
function doFunction()
{
    var i =10;
    do
    {
alert("do loop i = " + i);
i --; //subtract from 10
    }
    while (i > 0);
}
